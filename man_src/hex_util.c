#include "cub.h"

int	rgb_translate(int color)
{
	int r;
	int g;
	int b;

	r = (color >> 16) & 0xFF;
	g = (color >> 8) & 0xFF;
	b = color & 0xFF;
	return (r << 16 | g << 8 | b);
}